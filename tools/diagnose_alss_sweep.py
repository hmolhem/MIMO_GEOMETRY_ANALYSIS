"""
Diagnostic sweep for ALSS mode/tau behavior.

Purpose
-------
This script tests whether ALSS performance is sensitive to:
- alss_mode: zero vs ar1
- alss_tau: shrinkage strength

It is intended for diagnostics only. Results generated by this script are not
paper-ready unless rerun with a validated protocol and sufficient Monte Carlo
trials.

Recommended first run from repository root:

    $env:PYTHONPATH = "$(Get-Location)\\src;$(Get-Location)"
    python tools\\diagnose_alss_sweep.py --trials 10 --output-dir "$env:TEMP\\mimo_alss_diag_trial10"

The output CSV should be written outside the repository, typically under $env:TEMP.
"""

